#include "libft.h"

int	ft_atoi(const char *s)
{
	int	ret;
	int	sig;

	ret = 0;
	sig = 1;
	while (*s == ' ' || *s == '\r' || *s == '\t'
		|| *s == '\f' || *s == '\v' || *s == '\n')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sig = -1;
		s++;
	}
	while (ft_isdigit(*s))
	{
		ret = ret * 10 + ((*s - '0') * sig);
		s++;
	}
	return (ret);
}
